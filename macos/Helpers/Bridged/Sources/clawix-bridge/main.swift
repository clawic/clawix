    func startRemoteJob(
        jobId: String,
        requesterNodeId: String,
        workspacePath: String,
        prompt: String
    ) async throws -> RemoteJob {
        let workspace = URL(fileURLWithPath: workspacePath).standardizedFileURL.path
        guard meshStore.allowsWorkspace(workspace) else {
            throw RemoteMeshError.workspaceDenied
        }
        let chatId = UUID().uuidString
        var job = RemoteJob(
            id: jobId,
            requesterNodeId: requesterNodeId,
            workspacePath: workspace,
            prompt: prompt,
            status: .running,
            remoteChatId: chatId
        )
        meshStore.upsert(job: job)
        meshStore.append(event: RemoteJobEvent(jobId: jobId, type: "accepted", message: "Remote job accepted"))

        do {
            let result = try await backend.send(
                method: "thread/start",
                params: ThreadStartParams(
                    cwd: workspace,
                    model: nil,
                    approvalPolicy: nil,
                    sandbox: nil,
                    personalizationPreset: nil,
                    serviceTier: nil,
                    collaborationMode: nil
                ),
                expecting: ThreadStartResult.self
            )
            let threadId = result.thread.id
            chatByThread[threadId] = chatId
            threadByChat[chatId] = threadId
            remoteJobByThread[threadId] = jobId
            job.remoteThreadId = threadId
            job.updatedAt = Date()
            meshStore.upsert(job: job)
            ensureSnapshot(chatId: chatId, firstPrompt: prompt, cwd: workspace)
            appendMessage(chatId: chatId, message: WireMessage(
                id: UUID().uuidString,
                role: .user,
                content: prompt,
                streamingFinished: true,
                timestamp: Date()
            ))
            updateChat(chatId: chatId) {
                $0.hasActiveTurn = true
                $0.lastMessagePreview = String(prompt.prefix(140))
                $0.lastMessageAt = Date()
                $0.threadId = threadId
            }
            meshStore.append(event: RemoteJobEvent(jobId: jobId, type: "threadStarted", message: threadId))
            let turn = try await backend.send(
                method: "turn/start",
                params: TurnStartParams(
                    threadId: threadId,
                    input: [.text(prompt)],
                    model: nil,
                    effort: nil,
                    serviceTier: nil,
                    collaborationMode: nil
                ),
                expecting: TurnStartResult.self
            )
            activeTurnByThread[threadId] = turn.turn.id
            meshStore.append(event: RemoteJobEvent(jobId: jobId, type: "turnStarted", message: turn.turn.id))
            job.updatedAt = Date()
            meshStore.upsert(job: job)
            return job
        } catch {
            job.status = .failed
            job.errorMessage = "\(error)"
            job.updatedAt = Date()
            meshStore.upsert(job: job)
            meshStore.append(event: RemoteJobEvent(jobId: jobId, type: "failed", message: "\(error)"))
            throw error
        }
    }

    func cancelRemoteJob(jobId: String) async {
        guard var job = meshStore.job(id: jobId) else { return }
        if let threadId = job.remoteThreadId,
           let chatId = chatByThread[threadId],
           let turnId = activeTurnByThread[threadId] {
            activeTurnByThread[threadId] = nil
            do {
                _ = try await backend.send(
                    method: "turn/interrupt",
                    params: TurnInterruptParams(threadId: threadId, turnId: turnId),
                    expecting: EmptyResponse.self
                )
            } catch {
                BridgeLog.write("mesh job cancel failed \(error)")
            }
            updateChat(chatId: chatId) {
                $0.hasActiveTurn = false
                $0.lastTurnInterrupted = true
            }
        }
        job.status = .cancelled
        job.updatedAt = Date()
        meshStore.upsert(job: job)
        meshStore.append(event: RemoteJobEvent(jobId: jobId, type: "cancelled", message: "Remote job cancelled"))
    }

    private func reloadThreads() async -> Bool {
        do {
            let pageSize = 160
            let maxPages = 10
            var collected: [AgentThreadSummary] = []
            var seenIds = Set<String>()
            var cursor: String? = nil
            var page = 0

            repeat {
                let response = try await backend.send(
                    method: "thread/list",
                    params: ThreadListParams(
                        archived: false,
                        cursor: cursor,
                        cwd: nil,
                        limit: pageSize,
                        modelProviders: nil,
                        searchTerm: nil,
                        sortDirection: "desc",
                        sortKey: "updated_at",
                        sourceKinds: nil,
                        useStateDbOnly: nil
                    ),
                    expecting: ThreadListResponse.self,
                    timeoutSeconds: threadListTimeoutSeconds
                )
                for thread in response.data where seenIds.insert(thread.id).inserted {
                    collected.append(thread)
                }
                cursor = response.nextCursor
                page += 1
            } while cursor != nil && page < maxPages

            let runtimeSnapshots = collected.map(snapshot(from:))
            BridgeLog.write("thread-list ok count=\(runtimeSnapshots.count) source=runtime pages=\(page)")
            chatsSubject.send(runtimeSnapshots)
            lastChatsPublishedAt = Date()
            return true
        } catch {
            BridgeLog.write("thread-list failed \(error)")
            // Surface to peers as an error state rather than silently
            // leaving them on `syncing` forever. The bootstrap caller
            // will move us to `.ready` only on success, so on failure
            // the iPhone sees an actionable message instead of a
            // perpetual spinner.
            stateSubject.send(.error("Couldn't load chats: \(shortReason(error))"))
            return false
        }
    }

    private func title(fromFirstUserMessage message: String?, fallback: String) -> String {
        let trimmed = (message ?? "").trimmingCharacters(in: .whitespacesAndNewlines)
        if !trimmed.isEmpty {
            let firstLine = trimmed.split(separator: "\n", maxSplits: 1, omittingEmptySubsequences: true).first
            return String((firstLine.map(String.init) ?? trimmed).prefix(80))
        }
        return fallback
    }

    private func snapshot(from thread: AgentThreadSummary) -> BridgeChatSnapshot {
        let chatId = chatByThread[thread.id] ?? UUID().uuidString
        chatByThread[thread.id] = chatId
        threadByChat[chatId] = thread.id
        let archived = thread.archived ?? false
        return BridgeChatSnapshot(
            chat: WireSession(
                id: chatId,
                title: title(for: thread),
                createdAt: thread.updatedDate,
                isPinned: false,
                isArchived: archived,
                hasActiveTurn: activeTurnByThread[thread.id] != nil,
                lastMessageAt: thread.updatedDate,
                lastMessagePreview: thread.preview,
                cwd: thread.cwd,
                threadId: thread.id
            ),
            messages: existingMessages(chatId: chatId)
        )
    }

    private func title(for thread: AgentThreadSummary) -> String {
        if let name = thread.name?.trimmingCharacters(in: .whitespacesAndNewlines), !name.isEmpty {
            return name
        }
        let preview = thread.preview.trimmingCharacters(in: .whitespacesAndNewlines)
        return preview.isEmpty ? "Conversation" : String(preview.prefix(60))
    }

    private func ensureThread(chatId: String, firstPrompt: String) async throws -> String {
        if let threadId = threadByChat[chatId] {
            return threadId
        }
        let result = try await backend.send(
            method: "thread/start",
            params: ThreadStartParams(
                cwd: FileManager.default.homeDirectoryForCurrentUser.path,
                model: nil,
                approvalPolicy: nil,
                sandbox: nil,
                personalizationPreset: nil,
                serviceTier: nil,
                collaborationMode: nil
            ),
            expecting: ThreadStartResult.self
        )
        let threadId = result.thread.id
        chatByThread[threadId] = chatId
        threadByChat[chatId] = threadId
        ensureSnapshot(chatId: chatId, firstPrompt: firstPrompt, cwd: result.thread.cwd)
        updateSnapshot(chatId: chatId) { snap in
            let trimmed = firstPrompt.trimmingCharacters(in: .whitespacesAndNewlines)
            snap.chat.title = trimmed.isEmpty ? "Conversation" : String(trimmed.prefix(60))
            snap.chat.cwd = result.thread.cwd
            // Stamp the freshly minted Codex thread id onto the wire
            // chat so desktop clients can reconcile pin / project
            // metadata against `BackendStateReader` going forward.
            snap.chat.threadId = threadId
        }
        return threadId
    }

    private func ensureSnapshot(chatId: String, firstPrompt: String, cwd: String?) {
        guard !bridgeChatsCurrent.contains(where: { $0.id == chatId }) else { return }
        let trimmed = firstPrompt.trimmingCharacters(in: .whitespacesAndNewlines)
        var snapshots = bridgeChatsCurrent
        snapshots.insert(
            BridgeChatSnapshot(
                chat: WireSession(
                    id: chatId,
                    title: trimmed.isEmpty ? "Conversation" : String(trimmed.prefix(60)),
                    createdAt: Date(),
                    isArchived: false,
                    hasActiveTurn: false,
                    lastMessageAt: Date(),
                    lastMessagePreview: String(trimmed.prefix(140)),
                    cwd: cwd
                ),
                messages: []
            ),
            at: 0
        )
        chatsSubject.send(snapshots)
    }

