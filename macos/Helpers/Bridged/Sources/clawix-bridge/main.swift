    private func handle(_ event: BackendEvent) {
        switch event {
        case let .request(id, method, _):
            if method == "item/tool/requestUserInput" {
                try? backend.resolve(id: id, result: ToolRequestUserInputResponse(answers: [:]))
            }
        case let .notification(method, params):
            handleNotification(method: method, params: params)
        }
    }

    private func handleNotification(method: String, params: JSONValue?) {
        switch method {
        case "turn/started":
            guard let payload = try? params?.decode(TurnEnvelope.self),
                  let chatId = chatByThread[payload.threadId]
            else { return }
            activeTurnByThread[payload.threadId] = payload.turn.id
            let assistantId = ensureAssistant(chatId: chatId, threadId: payload.threadId)
            // Stamp the live "Worked for Xs" counter on the placeholder so
            // the chat row's header ticks live while the model is working.
            // `endedAt` stays nil; `turn/completed` freezes it.
            let started = Date()
            updateMessage(chatId: chatId, messageId: assistantId) { msg in
                if msg.workSummary == nil {
                    msg.workSummary = WireWorkSummary(
                        startedAt: started,
                        endedAt: nil,
                        items: []
                    )
                }
            }
            updateChat(chatId: chatId) { $0.hasActiveTurn = true }
        case "item/agentMessage/delta":
            guard let payload = try? params?.decode(AgentMessageDelta.self),
                  let chatId = chatByThread[payload.threadId]
            else { return }
            if let jobId = remoteJobByThread[payload.threadId] {
                if var job = meshStore.job(id: jobId) {
                    job.resultText = (job.resultText ?? "") + payload.delta
                    job.updatedAt = Date()
                    meshStore.upsert(job: job)
                }
                meshStore.append(event: RemoteJobEvent(jobId: jobId, type: "delta", message: payload.delta))
            }
            let assistantId = ensureAssistant(chatId: chatId, threadId: payload.threadId)
            updateMessage(chatId: chatId, messageId: assistantId) { msg in
                msg.content += payload.delta
                msg.streamingFinished = false
            }
        case "item/reasoning/textDelta", "item/reasoning/summaryTextDelta":
            guard let payload = try? params?.decode(ReasoningTextDelta.self),
                  let chatId = chatByThread[payload.threadId]
            else { return }
            let assistantId = ensureAssistant(chatId: chatId, threadId: payload.threadId)
            updateMessage(chatId: chatId, messageId: assistantId) { msg in
                msg.reasoningText += payload.delta
                msg.streamingFinished = false
            }
        case "item/completed":
            guard let payload = try? params?.decode(ItemEnvelope.self),
                  payload.item.type == "agentMessage",
                  let chatId = chatByThread[payload.threadId],
                  let assistantId = activeAssistantIdByThread[payload.threadId]
            else { return }
            updateMessage(chatId: chatId, messageId: assistantId) { msg in
                if let text = payload.item.text, !text.isEmpty {
                    msg.content = text
                }
                msg.streamingFinished = true
            }
        case "turn/completed":
            guard let payload = try? params?.decode(TurnEnvelope.self),
                  let chatId = chatByThread[payload.threadId]
            else { return }
            // Freeze the work-summary counter at turn close so the live
            // ticker stops; without this the chat row keeps incrementing
            // even though the agent is done.
            if let assistantId = activeAssistantIdByThread[payload.threadId] {
                let ended = Date()
                updateMessage(chatId: chatId, messageId: assistantId) { msg in
                    if msg.workSummary != nil, msg.workSummary?.endedAt == nil {
                        msg.workSummary?.endedAt = ended
                    }
                }
            }
            activeTurnByThread[payload.threadId] = nil
            activeAssistantIdByThread[payload.threadId] = nil
            updateChat(chatId: chatId) {
                $0.hasActiveTurn = false
                $0.lastTurnInterrupted = false
            }
            if let jobId = remoteJobByThread[payload.threadId] {
                var job = meshStore.job(id: jobId) ?? RemoteJob(
                    id: jobId,
                    requesterNodeId: "",
                    workspacePath: "",
                    prompt: ""
                )
                job.status = .completed
                if job.resultText == nil || job.resultText?.isEmpty == true {
                    job.resultText = existingMessages(chatId: chatId).last(where: { $0.role == .assistant })?.content
                }
                job.updatedAt = Date()
                meshStore.upsert(job: job)
                meshStore.append(event: RemoteJobEvent(jobId: jobId, type: "completed", message: job.resultText ?? ""))
                remoteJobByThread[payload.threadId] = nil
            }
        case "account/rateLimits/updated":
            guard let payload = try? params?.decode(DaemonAccountRateLimitsUpdatedNotification.self) else { return }
            rateLimitsSubject.send(WireRateLimitsPayload(
                snapshot: wireSnapshot(from: payload.rateLimits),
                byLimitId: wireByLimitId(from: payload.rateLimitsByLimitId)
            ))
        default:
            break
        }
    }

    @discardableResult
    private func ensureAssistant(chatId: String, threadId: String) -> String {
        if let id = activeAssistantIdByThread[threadId] { return id }
        let id = UUID().uuidString
        activeAssistantIdByThread[threadId] = id
        appendMessage(chatId: chatId, message: WireMessage(
            id: id,
            role: .assistant,
            content: "",
            streamingFinished: false,
            timestamp: Date()
        ))
        return id
    }

    private func appendError(chatId: String, message: String) {
        appendMessage(chatId: chatId, message: WireMessage(
            id: UUID().uuidString,
            role: .assistant,
            content: message,
            streamingFinished: true,
            isError: true,
            timestamp: Date()
        ))
        updateChat(chatId: chatId) { $0.hasActiveTurn = false }
    }

    private func existingMessages(chatId: String) -> [WireMessage] {
        bridgeChatsCurrent.first(where: { $0.id == chatId })?.messages ?? []
    }

    private func appendMessage(chatId: String, message: WireMessage) {
        updateSnapshot(chatId: chatId) { snap in
            snap.messages.append(message)
            snap.chat.lastMessageAt = message.timestamp
            snap.chat.lastMessagePreview = String(message.content.prefix(140))
        }
    }

    private func updateMessage(chatId: String, messageId: String, mutate: (inout WireMessage) -> Void) {
        updateSnapshot(chatId: chatId) { snap in
            guard let idx = snap.messages.firstIndex(where: { $0.id == messageId }) else { return }
            mutate(&snap.messages[idx])
            let msg = snap.messages[idx]
            snap.chat.lastMessageAt = msg.timestamp
            snap.chat.lastMessagePreview = String(msg.content.prefix(140))
        }
    }

    private func updateChat(chatId: String, mutate: (inout WireSession) -> Void) {
        updateSnapshot(chatId: chatId) { mutate(&$0.chat) }
    }

    private func updateSnapshot(chatId: String, mutate: (inout MutableSnapshot) -> Void) {
        var snapshots = bridgeChatsCurrent
        guard let index = snapshots.firstIndex(where: { $0.id == chatId }) else { return }
        var mutable = MutableSnapshot(snapshots[index])
        mutate(&mutable)
        snapshots[index] = mutable.snapshot
        chatsSubject.send(snapshots)
    }
}

private func preserveUserAttachments(existing: [WireMessage], incoming: [WireMessage]) -> [WireMessage] {
    var remaining = existing.filter { $0.role == .user && !$0.attachments.isEmpty }
    return incoming.map { message in
        guard message.role == .user, message.attachments.isEmpty else { return message }
        if let idx = remaining.firstIndex(where: { $0.id == message.id }) {
            let source = remaining.remove(at: idx)
            return preserveUserAttachments(existing: source, incoming: message)
        }
        if let idx = remaining.firstIndex(where: { isLikelySameUserPrompt($0.content, message.content) }) {
            let source = remaining.remove(at: idx)
            return preserveUserAttachments(existing: source, incoming: message)
        }
        return message
    }
}

private func preserveUserAttachments(existing: WireMessage, incoming: WireMessage) -> WireMessage {
    guard incoming.role == .user,
          incoming.attachments.isEmpty,
          !existing.attachments.isEmpty
    else { return incoming }
    return WireMessage(
        id: incoming.id,
        role: incoming.role,
        content: incoming.content,
        reasoningText: incoming.reasoningText,
        streamingFinished: incoming.streamingFinished,
        isError: incoming.isError,
        timestamp: incoming.timestamp,
        timeline: incoming.timeline,
        workSummary: incoming.workSummary,
        audioRef: incoming.audioRef ?? existing.audioRef,
        attachments: existing.attachments
    )
}

private func isLikelySameUserPrompt(_ lhs: String, _ rhs: String) -> Bool {
    let left = normalizedPrompt(lhs)
    let right = normalizedPrompt(rhs)
    guard !left.isEmpty, !right.isEmpty else { return false }
    return left == right || left.hasSuffix(right) || right.hasSuffix(left)
}

private func normalizedPrompt(_ text: String) -> String {
    var value = text.trimmingCharacters(in: .whitespacesAndNewlines)
    let labels = ["[image fallback]", "[image]", "[images]"]
    for label in labels where value.hasPrefix(label) {
        value = String(value.dropFirst(label.count)).trimmingCharacters(in: .whitespacesAndNewlines)
        break
    }
    if value.hasPrefix("Attached images are preserved in Clawix UI,"),
       let range = value.range(of: "\n\n") {
        value = String(value[range.upperBound...]).trimmingCharacters(in: .whitespacesAndNewlines)
    }
    return value
}

private struct MutableSnapshot {
    var chat: WireSession
    var messages: [WireMessage]

    init(_ snapshot: BridgeChatSnapshot) {
        self.chat = snapshot.chat
        self.messages = snapshot.messages
    }

    var snapshot: BridgeChatSnapshot {
        BridgeChatSnapshot(chat: chat, messages: messages)
    }
}
