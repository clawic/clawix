    func startRemoteJob(
        jobId: String,
        requesterNodeId: String,
        workspacePath: String,
        prompt: String
    ) async throws -> RemoteJob {
        let workspace = URL(fileURLWithPath: workspacePath).standardizedFileURL.path
        guard meshStore.allowsWorkspace(workspace) else {
            throw RemoteMeshError.workspaceDenied
        }
        let chatId = UUID().uuidString
        var job = RemoteJob(
            id: jobId,
            requesterNodeId: requesterNodeId,
            workspacePath: workspace,
            prompt: prompt,
            status: .running,
            remoteChatId: chatId
        )
        meshStore.upsert(job: job)
        meshStore.append(event: RemoteJobEvent(jobId: jobId, type: "accepted", message: "Remote job accepted"))

        do {
            let result = try await backend.send(
                method: "thread/start",
                params: ThreadStartParams(
                    cwd: workspace,
                    model: nil,
                    approvalPolicy: nil,
                    sandbox: nil,
                    personality: nil,
                    serviceTier: nil,
                    collaborationMode: nil
                ),
                expecting: ThreadStartResult.self
            )
            let threadId = result.thread.id
            chatByThread[threadId] = chatId
            threadByChat[chatId] = threadId
            remoteJobByThread[threadId] = jobId
            job.remoteThreadId = threadId
            job.updatedAt = Date()
            meshStore.upsert(job: job)
            ensureSnapshot(chatId: chatId, firstPrompt: prompt, cwd: workspace)
            appendMessage(chatId: chatId, message: WireMessage(
                id: UUID().uuidString,
                role: .user,
                content: prompt,
                streamingFinished: true,
                timestamp: Date()
            ))
            updateChat(chatId: chatId) {
                $0.hasActiveTurn = true
                $0.lastMessagePreview = String(prompt.prefix(140))
                $0.lastMessageAt = Date()
                $0.threadId = threadId
            }
            meshStore.append(event: RemoteJobEvent(jobId: jobId, type: "threadStarted", message: threadId))
            let turn = try await backend.send(
                method: "turn/start",
                params: TurnStartParams(
                    threadId: threadId,
                    input: [.text(prompt)],
                    model: nil,
                    effort: nil,
                    serviceTier: nil,
                    collaborationMode: nil
                ),
                expecting: TurnStartResult.self
            )
            activeTurnByThread[threadId] = turn.turn.id
            meshStore.append(event: RemoteJobEvent(jobId: jobId, type: "turnStarted", message: turn.turn.id))
            job.updatedAt = Date()
            meshStore.upsert(job: job)
            return job
        } catch {
            job.status = .failed
            job.errorMessage = "\(error)"
            job.updatedAt = Date()
            meshStore.upsert(job: job)
            meshStore.append(event: RemoteJobEvent(jobId: jobId, type: "failed", message: "\(error)"))
            throw error
        }
    }

    func cancelRemoteJob(jobId: String) async {
        guard var job = meshStore.job(id: jobId) else { return }
        if let threadId = job.remoteThreadId,
           let chatId = chatByThread[threadId],
           let turnId = activeTurnByThread[threadId] {
            activeTurnByThread[threadId] = nil
            do {
                _ = try await backend.send(
                    method: "turn/interrupt",
                    params: TurnInterruptParams(threadId: threadId, turnId: turnId),
                    expecting: EmptyResponse.self
                )
            } catch {
                BridgedLog.write("mesh job cancel failed \(error)")
            }
            updateChat(chatId: chatId) {
                $0.hasActiveTurn = false
                $0.lastTurnInterrupted = true
            }
        }
        job.status = .cancelled
        job.updatedAt = Date()
        meshStore.upsert(job: job)
        meshStore.append(event: RemoteJobEvent(jobId: jobId, type: "cancelled", message: "Remote job cancelled"))
    }

    private func reloadThreads() async -> Bool {
        // Re-read pins right before the publish: even with the
        // file-watcher in place, this catches the case where the
        // watcher hasn't been installed yet (state file didn't exist
        // at boot but does now) and also pays a cheap one-shot cost
        // to keep the pin set fresh on every reload triggered from
        // user interaction.
        refreshPinnedThreadIds()
        installPinnedStateWatcher()
        do {
            let pageSize = 160
            let maxPages = 10
            var collected: [AgentThreadSummary] = []
            var seenIds = Set<String>()
            var cursor: String? = nil
            var page = 0

            repeat {
                let response = try await backend.send(
                    method: "thread/list",
                    params: ThreadListParams(
                        archived: false,
                        cursor: cursor,
                        cwd: nil,
                        limit: pageSize,
                        modelProviders: nil,
                        searchTerm: nil,
                        sortDirection: "desc",
                        sortKey: "updated_at",
                        sourceKinds: nil,
                        useStateDbOnly: true
                    ),
                    expecting: ThreadListResponse.self,
                    timeoutSeconds: threadListTimeoutSeconds
                )
                for thread in response.data where seenIds.insert(thread.id).inserted {
                    collected.append(thread)
                }
                cursor = response.nextCursor
                page += 1
            } while cursor != nil && page < maxPages

            let snapshots = collected.map(snapshot(from:))
            BridgedLog.write("thread-list ok count=\(snapshots.count) pages=\(page) pinned=\(pinnedThreadIds.count)")
            chatsSubject.send(snapshots)
            lastChatsPublishedAt = Date()
            return true
        } catch {
            BridgedLog.write("thread-list failed \(error)")
            let fallback = loadRolloutFallbackSnapshots(limit: 1_200)
            if !fallback.isEmpty {
                BridgedLog.write("thread-list fallback from rollouts count=\(fallback.count)")
                chatsSubject.send(fallback)
                lastChatsPublishedAt = Date()
                return true
            }
            // Surface to peers as an error state rather than silently
            // leaving them on `syncing` forever. The bootstrap caller
            // will move us to `.ready` only on success, so on failure
            // the iPhone sees an actionable message instead of a
            // perpetual spinner.
            stateSubject.send(.error("Couldn't load chats: \(shortReason(error))"))
            return false
        }
    }

    private func loadRolloutFallbackSnapshots(limit: Int) -> [BridgeChatSnapshot] {
        refreshPinnedThreadIds()
        let files = RolloutHistory.localRolloutFiles(limit: max(limit * 2, limit))
        guard !files.isEmpty else { return [] }
        var snapshots: [BridgeChatSnapshot] = []
        var seen = Set<String>()
        for file in files {
            guard snapshots.count < limit,
                  let threadId = RolloutHistory.threadId(from: file),
                  seen.insert(threadId).inserted else { continue }
            let meta = RolloutHistory.metadata(path: file)
            let chatId = chatByThread[threadId] ?? UUID().uuidString
            chatByThread[threadId] = chatId
            threadByChat[chatId] = threadId
            rolloutPathByThread[threadId] = file.path

            let fileDate = (try? file.resourceValues(forKeys: [.contentModificationDateKey]).contentModificationDate) ?? Date()
            let tail: (messages: [WireMessage], lastTurnInterrupted: Bool) = ([], false)
            let createdAt = meta.startedAt ?? tail.messages.first?.timestamp ?? fileDate
            let last = tail.messages.last
            let title = title(fromFirstUserMessage: meta.firstUserMessage, fallback: file.deletingPathExtension().lastPathComponent)
            snapshots.append(BridgeChatSnapshot(
                chat: WireChat(
                    id: chatId,
                    title: title,
                    createdAt: createdAt,
                    isPinned: pinnedThreadIds.contains(threadId),
                    isArchived: false,
                    hasActiveTurn: false,
                    lastMessageAt: last?.timestamp ?? createdAt,
                    lastMessagePreview: last.map { String($0.content.prefix(140)) },
                    cwd: meta.cwd,
                    lastTurnInterrupted: tail.lastTurnInterrupted,
                    threadId: threadId
                ),
                messages: tail.messages
            ))
        }
        return snapshots.sorted {
            ($0.chat.lastMessageAt ?? $0.chat.createdAt) > ($1.chat.lastMessageAt ?? $1.chat.createdAt)
        }
    }

    private func title(fromFirstUserMessage message: String?, fallback: String) -> String {
        let trimmed = (message ?? "").trimmingCharacters(in: .whitespacesAndNewlines)
        if !trimmed.isEmpty {
            let firstLine = trimmed.split(separator: "\n", maxSplits: 1, omittingEmptySubsequences: true).first
            return String((firstLine.map(String.init) ?? trimmed).prefix(80))
        }
        return fallback
    }

    private func snapshot(from thread: AgentThreadSummary) -> BridgeChatSnapshot {
        let chatId = chatByThread[thread.id] ?? UUID().uuidString
        chatByThread[thread.id] = chatId
        threadByChat[chatId] = thread.id
        if let path = thread.path { rolloutPathByThread[thread.id] = path }
        let archived = thread.archived ?? false
        // Pinned chats stay pinned while archived in Codex's data
        // model, but the GUI hides the pin badge for archived rows
        // (mirroring `chatFromThread` in `AppState.swift`). Clamp
        // here so connected clients don't have to repeat that rule.
        let isPinned = !archived && pinnedThreadIds.contains(thread.id)
        return BridgeChatSnapshot(
            chat: WireChat(
                id: chatId,
                title: title(for: thread),
                createdAt: thread.updatedDate,
                isPinned: isPinned,
                isArchived: archived,
                hasActiveTurn: activeTurnByThread[thread.id] != nil,
                lastMessageAt: thread.updatedDate,
                lastMessagePreview: thread.preview,
                cwd: thread.cwd,
                threadId: thread.id
            ),
            messages: existingMessages(chatId: chatId)
        )
    }

    private func title(for thread: AgentThreadSummary) -> String {
        if let name = thread.name?.trimmingCharacters(in: .whitespacesAndNewlines), !name.isEmpty {
            return name
        }
        let preview = thread.preview.trimmingCharacters(in: .whitespacesAndNewlines)
        return preview.isEmpty ? "Conversation" : String(preview.prefix(60))
    }

    private func ensureThread(chatId: String, firstPrompt: String) async throws -> String {
        if let threadId = threadByChat[chatId] {
            return threadId
        }
        let result = try await backend.send(
            method: "thread/start",
            params: ThreadStartParams(
                cwd: FileManager.default.homeDirectoryForCurrentUser.path,
                model: nil,
                approvalPolicy: nil,
                sandbox: nil,
                personality: nil,
                serviceTier: nil,
                collaborationMode: nil
            ),
            expecting: ThreadStartResult.self
        )
        let threadId = result.thread.id
        chatByThread[threadId] = chatId
        threadByChat[chatId] = threadId
        ensureSnapshot(chatId: chatId, firstPrompt: firstPrompt, cwd: result.thread.cwd)
        updateSnapshot(chatId: chatId) { snap in
            let trimmed = firstPrompt.trimmingCharacters(in: .whitespacesAndNewlines)
            snap.chat.title = trimmed.isEmpty ? "Conversation" : String(trimmed.prefix(60))
            snap.chat.cwd = result.thread.cwd
            // Stamp the freshly minted Codex thread id onto the wire
            // chat so desktop clients can reconcile pin / project
            // metadata against `BackendStateReader` going forward.
            snap.chat.threadId = threadId
        }
        return threadId
    }

    private func ensureSnapshot(chatId: String, firstPrompt: String, cwd: String?) {
        guard !bridgeChatsCurrent.contains(where: { $0.id == chatId }) else { return }
        let trimmed = firstPrompt.trimmingCharacters(in: .whitespacesAndNewlines)
        var snapshots = bridgeChatsCurrent
        snapshots.insert(
            BridgeChatSnapshot(
                chat: WireChat(
                    id: chatId,
                    title: trimmed.isEmpty ? "Conversation" : String(trimmed.prefix(60)),
                    createdAt: Date(),
                    isArchived: false,
                    hasActiveTurn: false,
                    lastMessageAt: Date(),
                    lastMessagePreview: String(trimmed.prefix(140)),
                    cwd: cwd
                ),
                messages: []
            ),
            at: 0
        )
        chatsSubject.send(snapshots)
    }

    private func hydrate(chatId: String) {
        guard let threadId = threadByChat[chatId],
              let path = rolloutPathByThread[threadId]
        else { return }
        let existing = existingMessages(chatId: chatId)
        let result = RolloutHistory.readTail(path: URL(fileURLWithPath: path))
        guard !result.messages.isEmpty || result.lastTurnInterrupted else { return }
        // Apply the canonical messages synchronously before returning so
        // the imminent `bus.subscribe(chatId)` call in
        // `BridgeIntent.dispatch` reads the full transcript out of
        // `bridgeChatsCurrent` and the first frame the client gets is a
        // populated `messagesSnapshot`. Wrapping the apply in a Task (as
        // before) made the snapshot ship empty and the publisher tick
        // that landed after the Task ran emitted N `messageAppended`
        // frames in a row, which the client paints one row per render.
        updateSnapshot(chatId: chatId) { snap in
            snap.messages = preserveUserAttachments(existing: existing, incoming: result.messages)
            snap.chat.lastTurnInterrupted = result.lastTurnInterrupted
            if let last = snap.messages.last {
                snap.chat.lastMessageAt = last.timestamp
                snap.chat.lastMessagePreview = String(last.content.prefix(140))
            }
        }
        // Audio refs from previous sessions live in the AudioMessageStore.
        // Rollout rebuild mints fresh message ids so we can't key by id;
        // instead we walk audio entries (chronological) and pair them up
        // with rebuilt user_messages, prefering content-equality and
        // breaking ties by ordinal. Missing entries decay gracefully:
        // the user message just shows without a bubble, the bytes are
        // still on disk and `requestAudio` still works if a client knows
        // the audioId from another route.
        //
        // Done after the message apply because `AudioMessageStore.entries`
        // is actor-isolated (async). Patching `audioRef` doesn't change
        // `messageCount` nor the last message's content/reasoning, so the
        // follow-up publisher tick doesn't trigger any spurious
        // `messageAppended` / `messageStreaming` frame from `BridgeBus`.
        Task { @MainActor in
            let audioEntries = await AudioMessageStore.shared.entries(forThread: threadId)
            guard !audioEntries.isEmpty else { return }
            self.updateSnapshot(chatId: chatId) { snap in
                var unmatched = audioEntries
                for idx in snap.messages.indices
                    where snap.messages[idx].role == .user
                       && snap.messages[idx].audioRef == nil {
                    let content = snap.messages[idx].content
                    let pickIdx: Int? = {
                        if let exact = unmatched.firstIndex(where: { $0.transcript == content }) {
                            return exact
                        }
                        return unmatched.indices.first
                    }()
                    if let pickIdx {
                        snap.messages[idx].audioRef = unmatched[pickIdx].wireRef
                        unmatched.remove(at: pickIdx)
                    }
                }
            }
        }
    }

    private func startEvents() {
        let stream = backend.events
        Task { @MainActor in
            for await event in stream {
                handle(event)
            }
        }
    }

    private func handle(_ event: BackendEvent) {
        switch event {
        case let .request(id, method, _):
            if method == "item/tool/requestUserInput" {
                try? backend.resolve(id: id, result: ToolRequestUserInputResponse(answers: [:]))
            }
        case let .notification(method, params):
            handleNotification(method: method, params: params)
        }
    }

    private func handleNotification(method: String, params: JSONValue?) {
        switch method {
        case "turn/started":
            guard let payload = try? params?.decode(TurnEnvelope.self),
                  let chatId = chatByThread[payload.threadId]
            else { return }
            activeTurnByThread[payload.threadId] = payload.turn.id
            let assistantId = ensureAssistant(chatId: chatId, threadId: payload.threadId)
            // Stamp the live "Worked for Xs" counter on the placeholder so
            // the chat row's header ticks live while the model is working.
            // `endedAt` stays nil; `turn/completed` freezes it.
            let started = Date()
            updateMessage(chatId: chatId, messageId: assistantId) { msg in
                if msg.workSummary == nil {
                    msg.workSummary = WireWorkSummary(
                        startedAt: started,
                        endedAt: nil,
                        items: []
                    )
                }
            }
            updateChat(chatId: chatId) { $0.hasActiveTurn = true }
        case "item/agentMessage/delta":
            guard let payload = try? params?.decode(AgentMessageDelta.self),
                  let chatId = chatByThread[payload.threadId]
            else { return }
            if let jobId = remoteJobByThread[payload.threadId] {
                if var job = meshStore.job(id: jobId) {
                    job.resultText = (job.resultText ?? "") + payload.delta
                    job.updatedAt = Date()
                    meshStore.upsert(job: job)
                }
                meshStore.append(event: RemoteJobEvent(jobId: jobId, type: "delta", message: payload.delta))
            }
            let assistantId = ensureAssistant(chatId: chatId, threadId: payload.threadId)
            updateMessage(chatId: chatId, messageId: assistantId) { msg in
                msg.content += payload.delta
                msg.streamingFinished = false
            }
        case "item/reasoning/textDelta", "item/reasoning/summaryTextDelta":
            guard let payload = try? params?.decode(ReasoningTextDelta.self),
                  let chatId = chatByThread[payload.threadId]
            else { return }
            let assistantId = ensureAssistant(chatId: chatId, threadId: payload.threadId)
            updateMessage(chatId: chatId, messageId: assistantId) { msg in
                msg.reasoningText += payload.delta
                msg.streamingFinished = false
            }
        case "item/completed":
            guard let payload = try? params?.decode(ItemEnvelope.self),
                  payload.item.type == "agentMessage",
                  let chatId = chatByThread[payload.threadId],
                  let assistantId = activeAssistantIdByThread[payload.threadId]
            else { return }
            updateMessage(chatId: chatId, messageId: assistantId) { msg in
                if let text = payload.item.text, !text.isEmpty {
                    msg.content = text
                }
                msg.streamingFinished = true
            }
        case "turn/completed":
            guard let payload = try? params?.decode(TurnEnvelope.self),
                  let chatId = chatByThread[payload.threadId]
            else { return }
            // Freeze the work-summary counter at turn close so the live
            // ticker stops; without this the chat row keeps incrementing
            // even though the agent is done.
            if let assistantId = activeAssistantIdByThread[payload.threadId] {
                let ended = Date()
                updateMessage(chatId: chatId, messageId: assistantId) { msg in
                    if msg.workSummary != nil, msg.workSummary?.endedAt == nil {
                        msg.workSummary?.endedAt = ended
                    }
                }
            }
            activeTurnByThread[payload.threadId] = nil
            activeAssistantIdByThread[payload.threadId] = nil
            updateChat(chatId: chatId) {
                $0.hasActiveTurn = false
                $0.lastTurnInterrupted = false
            }
            if let jobId = remoteJobByThread[payload.threadId] {
                var job = meshStore.job(id: jobId) ?? RemoteJob(
                    id: jobId,
                    requesterNodeId: "",
                    workspacePath: "",
                    prompt: ""
                )
                job.status = .completed
                if job.resultText == nil || job.resultText?.isEmpty == true {
                    job.resultText = existingMessages(chatId: chatId).last(where: { $0.role == .assistant })?.content
                }
                job.updatedAt = Date()
                meshStore.upsert(job: job)
                meshStore.append(event: RemoteJobEvent(jobId: jobId, type: "completed", message: job.resultText ?? ""))
                remoteJobByThread[payload.threadId] = nil
            }
        case "account/rateLimits/updated":
            guard let payload = try? params?.decode(DaemonAccountRateLimitsUpdatedNotification.self) else { return }
            rateLimitsSubject.send(WireRateLimitsPayload(
                snapshot: wireSnapshot(from: payload.rateLimits),
                byLimitId: wireByLimitId(from: payload.rateLimitsByLimitId)
            ))
        default:
            break
        }
    }

    @discardableResult
    private func ensureAssistant(chatId: String, threadId: String) -> String {
        if let id = activeAssistantIdByThread[threadId] { return id }
        let id = UUID().uuidString
        activeAssistantIdByThread[threadId] = id
        appendMessage(chatId: chatId, message: WireMessage(
            id: id,
            role: .assistant,
            content: "",
            streamingFinished: false,
            timestamp: Date()
        ))
        return id
    }

    private func appendError(chatId: String, message: String) {
        appendMessage(chatId: chatId, message: WireMessage(
            id: UUID().uuidString,
            role: .assistant,
            content: message,
            streamingFinished: true,
            isError: true,
            timestamp: Date()
        ))
        updateChat(chatId: chatId) { $0.hasActiveTurn = false }
    }

    private func existingMessages(chatId: String) -> [WireMessage] {
        bridgeChatsCurrent.first(where: { $0.id == chatId })?.messages ?? []
    }

    private func appendMessage(chatId: String, message: WireMessage) {
        updateSnapshot(chatId: chatId) { snap in
            snap.messages.append(message)
            snap.chat.lastMessageAt = message.timestamp
            snap.chat.lastMessagePreview = String(message.content.prefix(140))
        }
    }

    private func updateMessage(chatId: String, messageId: String, mutate: (inout WireMessage) -> Void) {
        updateSnapshot(chatId: chatId) { snap in
            guard let idx = snap.messages.firstIndex(where: { $0.id == messageId }) else { return }
            mutate(&snap.messages[idx])
            let msg = snap.messages[idx]
            snap.chat.lastMessageAt = msg.timestamp
            snap.chat.lastMessagePreview = String(msg.content.prefix(140))
        }
    }

    private func updateChat(chatId: String, mutate: (inout WireChat) -> Void) {
        updateSnapshot(chatId: chatId) { mutate(&$0.chat) }
    }

    private func updateSnapshot(chatId: String, mutate: (inout MutableSnapshot) -> Void) {
        var snapshots = bridgeChatsCurrent
        guard let index = snapshots.firstIndex(where: { $0.id == chatId }) else { return }
        var mutable = MutableSnapshot(snapshots[index])
        mutate(&mutable)
        snapshots[index] = mutable.snapshot
        chatsSubject.send(snapshots)
    }
}

private func preserveUserAttachments(existing: [WireMessage], incoming: [WireMessage]) -> [WireMessage] {
    var remaining = existing.filter { $0.role == .user && !$0.attachments.isEmpty }
    return incoming.map { message in
        guard message.role == .user, message.attachments.isEmpty else { return message }
        if let idx = remaining.firstIndex(where: { $0.id == message.id }) {
            let source = remaining.remove(at: idx)
            return preserveUserAttachments(existing: source, incoming: message)
        }
        if let idx = remaining.firstIndex(where: { isLikelySameUserPrompt($0.content, message.content) }) {
            let source = remaining.remove(at: idx)
            return preserveUserAttachments(existing: source, incoming: message)
        }
        return message
    }
}

private func preserveUserAttachments(existing: WireMessage, incoming: WireMessage) -> WireMessage {
    guard incoming.role == .user,
          incoming.attachments.isEmpty,
          !existing.attachments.isEmpty
    else { return incoming }
    return WireMessage(
        id: incoming.id,
        role: incoming.role,
        content: incoming.content,
        reasoningText: incoming.reasoningText,
        streamingFinished: incoming.streamingFinished,
        isError: incoming.isError,
        timestamp: incoming.timestamp,
        timeline: incoming.timeline,
        workSummary: incoming.workSummary,
        audioRef: incoming.audioRef ?? existing.audioRef,
        attachments: existing.attachments
    )
}

private func isLikelySameUserPrompt(_ lhs: String, _ rhs: String) -> Bool {
    let left = normalizedPrompt(lhs)
    let right = normalizedPrompt(rhs)
    guard !left.isEmpty, !right.isEmpty else { return false }
    return left == right || left.hasSuffix(right) || right.hasSuffix(left)
}

private func normalizedPrompt(_ text: String) -> String {
    var value = text.trimmingCharacters(in: .whitespacesAndNewlines)
    let labels = ["[image fallback]", "[image]", "[images]"]
    for label in labels where value.hasPrefix(label) {
        value = String(value.dropFirst(label.count)).trimmingCharacters(in: .whitespacesAndNewlines)
        break
    }
    if value.hasPrefix("Attached images are preserved in Clawix UI,"),
       let range = value.range(of: "\n\n") {
        value = String(value[range.upperBound...]).trimmingCharacters(in: .whitespacesAndNewlines)
    }
    return value
}

